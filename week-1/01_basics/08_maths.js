// Math properties and constants
/**
 * Math.E
 * The base of natural logarithms, approximately 2.718.
 */
console.log("Math.E:", Math.E);

/**
 * Math.LN10
 * The natural logarithm of 10, approximately 2.302.
 */
console.log("Math.LN10:", Math.LN10);

/**
 * Math.LN2
 * The natural logarithm of 2, approximately 0.693.
 */
console.log("Math.LN2:", Math.LN2);

/**
 * Math.LOG10E
 * The base 10 logarithm of E, approximately 0.434.
 */
console.log("Math.LOG10E:", Math.LOG10E);

/**
 * Math.LOG2E
 * The base 2 logarithm of E, approximately 1.442.
 */
console.log("Math.LOG2E:", Math.LOG2E);

/**
 * Math.PI
 * The ratio of the circumference of a circle to its diameter, approximately 3.14159.
 */
console.log("Math.PI:", Math.PI);

/**
 * Math.SQRT1_2
 * The square root of 1/2, approximately 0.707.
 */
console.log("Math.SQRT1_2:", Math.SQRT1_2);

/**
 * Math.SQRT2
 * The square root of 2, approximately 1.414.
 */
console.log("Math.SQRT2:", Math.SQRT2);

// Math methods
/**
 * Math.abs(x)
 * Returns the absolute value of a number.
 */
console.log("Math.abs(-7.25):", Math.abs(-7.25)); // 7.25

/**
 * Math.acos(x)
 * Returns the arccosine of a number in radians.
 */
console.log("Math.acos(0.5):", Math.acos(0.5)); // 1.0471975511965979 (π/3)

/**
 * Math.asin(x)
 * Returns the arcsine of a number in radians.
 */
console.log("Math.asin(0.5):", Math.asin(0.5)); // 0.5235987755982989 (π/6)

/**
 * Math.atan(x)
 * Returns the arctangent of a number in radians.
 */
console.log("Math.atan(1):", Math.atan(1)); // 0.7853981633974483 (π/4)

/**
 * Math.atan2(y, x)
 * Returns the arctangent of the quotient of its arguments.
 */
console.log("Math.atan2(1, 1):", Math.atan2(1, 1)); // 0.7853981633974483 (π/4)

/**
 * Math.ceil(x)
 * Returns the smallest integer greater than or equal to a number.
 */
console.log("Math.ceil(1.4):", Math.ceil(1.4)); // 2

/**
 * Math.cos(x)
 * Returns the cosine of a number.
 */
console.log("Math.cos(Math.PI / 3):", Math.cos(Math.PI / 3)); // 0.5

/**
 * Math.exp(x)
 * Returns E^x, where x is the argument, and E is Euler's number.
 */
console.log("Math.exp(1):", Math.exp(1)); // 2.718281828459045

/**
 * Math.floor(x)
 * Returns the largest integer less than or equal to a number.
 */
console.log("Math.floor(1.6):", Math.floor(1.6)); // 1

/**
 * Math.log(x)
 * Returns the natural logarithm (base E) of a number.
 */
console.log("Math.log(Math.E):", Math.log(Math.E)); // 1

/**
 * Math.max(x, y, ...)
 * Returns the largest of the zero or more numbers given as input parameters.
 */
console.log("Math.max(10, 20, 30):", Math.max(10, 20, 30)); // 30

/**
 * Math.min(x, y, ...)
 * Returns the smallest of the zero or more numbers given as input parameters.
 */
console.log("Math.min(10, 20, 30):", Math.min(10, 20, 30)); // 10

/**
 * Math.pow(base, exponent)
 * Returns the base to the exponent power.
 */
console.log("Math.pow(2, 3):", Math.pow(2, 3)); // 8

/**
 * Math.random()
 * Returns a pseudo-random number between 0 and 1.
 */
console.log("Math.random():", Math.random());

/**
 * Math.round(x)
 * Returns the value of a number rounded to the nearest integer.
 */
console.log("Math.round(1.5):", Math.round(1.5)); // 2

/**
 * Math.sign(x)
 * Returns the sign of the number, indicating whether the number is positive, negative, or zero.
 */
console.log("Math.sign(-5):", Math.sign(-5)); // -1

/**
 * Math.sin(x)
 * Returns the sine of a number.
 */
console.log("Math.sin(Math.PI / 2):", Math.sin(Math.PI / 2)); // 1

/**
 * Math.sqrt(x)
 * Returns the square root of a number.
 */
console.log("Math.sqrt(4):", Math.sqrt(4)); // 2

/**
 * Math.tan(x)
 * Returns the tangent of a number.
 */
console.log("Math.tan(Math.PI / 4):", Math.tan(Math.PI / 4)); // 1

/**
 * Math.trunc(x)
 * Returns the integer part of a number by removing any fractional digits.
 */
console.log("Math.trunc(4.9):", Math.trunc(4.9)); // 4
